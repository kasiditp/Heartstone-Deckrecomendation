dust(X,_) :-
  card(X).

total_dust(DeckName,TotalDust) :-
  findall(Card,contain_with(DeckName,Card),CardList),
  findall(Dust,dust_cost(CardList,Dust),DustList),
  sum(DustList,0,TotalDust).

dust_cost(CardList,Dust) :-
  nth0(_, CardList, E, _),
  dust(E,Dust).

sum(L,R) :-
    sum(L,0,R).

sum([],F,F).
sum([H|T],F,R) :-
    (integer(F),integer(H)) -> F2 is F+H,sum(T,F2,R);
    F2 is F,
    sum(T,F2,R).

find_suitable_dust(InputAmount,Name,Amount):-
  deck_name(Name),
  total_dust(Name,Amount),
  \+ ((deck_name(Other),total_dust(Other,Amount),Amount>InputAmount)).
